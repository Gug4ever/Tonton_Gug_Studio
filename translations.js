let currentLang = localStorage.getItem('tgs-lang') || 'fr';

function t(key) {
  return LANG_DATA[currentLang]?.[key] ?? LANG_DATA['fr']?.[key] ?? key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('tgs-lang', lang);
  applyLang();
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.getAttribute('data-i18n'));
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-html'));
    if (val) el.innerHTML = val;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const val = t(el.getAttribute('data-i18n-ph'));
    if (val) el.placeholder = val;
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
  document.documentElement.lang = currentLang;
}

document.addEventListener('DOMContentLoaded', applyLang);
